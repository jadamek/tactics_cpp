#include "InputHandler.h"

//----------------------------------------------------------------------------
// - InputHandler Constructor
//----------------------------------------------------------------------------
InputHandler::InputHandler() :
    busy_(false),
    active_(true)
{}

//----------------------------------------------------------------------------
// - InputHandler Constructor
//----------------------------------------------------------------------------
InputHandler::~InputHandler()
{}

//----------------------------------------------------------------------------
// - Is Handler Busy?
//----------------------------------------------------------------------------
bool InputHandler::busy() const
{
    return busy_;
}

//----------------------------------------------------------------------------
// - Set Busy Flag
//----------------------------------------------------------------------------    
// * busy : flag indicating the handler is currently busy and will not be
// polling for input
//----------------------------------------------------------------------------
void InputHandler::setBusy(bool busy)
{
    busy_ = busy;
}

//----------------------------------------------------------------------------
// - Is Handler Active (Visible)?
//----------------------------------------------------------------------------
bool InputHandler::active() const
{
    return active_;
}

//----------------------------------------------------------------------------
// - Set Active Flag
//----------------------------------------------------------------------------    
// * active : flag indicating the handler is currently active
//----------------------------------------------------------------------------
void InputHandler::setActive(bool active)
{
    active_ = active;
}