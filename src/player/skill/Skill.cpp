#include "Skill.h"

//----------------------------------------------------------------------------
// Skill Constructor
//----------------------------------------------------------------------------
Skill::Skill() : 
    name_("SKILL"),
    casting_(false)
{}

//----------------------------------------------------------------------------
// Skill Constructor
//----------------------------------------------------------------------------
Skill::~Skill()
{}

//----------------------------------------------------------------------------
// Is Skill Casting?
//----------------------------------------------------------------------------
bool Skill::casting() const
{
    return casting_;
}

//----------------------------------------------------------------------------
// Get Skill's Name
//----------------------------------------------------------------------------
std::string Skill::name() const
{
    return name_;
}

//----------------------------------------------------------------------------
// Set Casting Flag
//----------------------------------------------------------------------------
// * casting : flag indicating the spell is currently being cast
//----------------------------------------------------------------------------
void Skill::setCastingStatus(bool casting)
{
    casting_ = casting;
}